# 1 Объявление переменной
example = "Топинамбур"
# 2 вывод первого символа строки
print(example[0])
# 3 вывод последнего символа строки
print(example[-1])
# 4 вывод второй половины строки
print(example[5:])
# 5 вывод строки в реверсе
print(example[-1::-1])
# 6 вывод каждого второго символа строки
print(example[1::2])
